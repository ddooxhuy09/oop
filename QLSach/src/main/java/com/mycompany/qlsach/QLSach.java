    private void jButton_ThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_ThemActionPerformed
        // TODO add your handling code here:
        String maSach = jTextField_MaSach.getText().trim();
        String tacGia = jTextField_TacGia.getText().trim();
        String tuaSach = jTextField_TuaSach.getText().trim();
        String nxb = jTextField_NXB.getText().trim();
        
        if (maSach.isEmpty() || tacGia.isEmpty() || tuaSach.isEmpty() || nxb.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin sách.");
            return; // Exit the method if inputs are not valid
        }


        // Step 4: Connect to the database and execute the SQL statement
        Connection ketNoi = KetNoiCSDL.layKetNoi();
        try {
        // Step 2: Check if the book ID already exists
        String checkSql = "SELECT COUNT(*) FROM SACH WHERE MASACH = ?";
        try (PreparedStatement checkStmt = ketNoi.prepareStatement(checkSql)) {
            checkStmt.setString(1, maSach);
            ResultSet rs = checkStmt.executeQuery();
            if (rs.next() && rs.getInt(1) > 0) {
                javax.swing.JOptionPane.showMessageDialog(this, "Mã sách đã tồn tại. Không thể thêm sách mới!");
                return; // Exit if the book ID already exists
            }
        }

        // Step 3: If not exists, insert the new book
        String insertSql = "INSERT INTO SACH (MASACH, TUASACH, TACGIA, NHAXUATBAN) VALUES (?, ?, ?, ?)";
            try (PreparedStatement insertStmt = ketNoi.prepareStatement(insertSql)) {
                insertStmt.setString(1, maSach);
                insertStmt.setString(2, tuaSach);
                insertStmt.setString(3, tacGia);
                insertStmt.setString(4, nxb);
                int rowsAffected = insertStmt.executeUpdate();
                if (rowsAffected > 0) {
                    javax.swing.JOptionPane.showMessageDialog(this, "Thêm sách thành công!");
                    layThongTinDauSach(); // Refresh the data table
                } else {
                    javax.swing.JOptionPane.showMessageDialog(this, "Thêm sách không thành công!");
                }
            }
        } catch (SQLException ex) {
            javax.swing.JOptionPane.showMessageDialog(this, "Lỗi khi thêm sách: " + ex.getMessage());
            ex.printStackTrace();
        } finally {
            try {
                if (ketNoi != null && !ketNoi.isClosed()) {
                    ketNoi.close();
                }
            } catch (SQLException ex) {
                System.out.println("Lỗi đóng kết nối cơ sở dữ liệu");
            }
        }
    }//GEN-LAST:event_jButton_ThemActionPerformed

