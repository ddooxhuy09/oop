    private void layThongTinDauSach() {
        DefaultTableModel dtm = (DefaultTableModel) jTableSach.getModel();
        dtm.setRowCount(0); // Uncomment this line if you want to clear old data on the table.

        Connection ketNoi = KetNoiCSDL.layKetNoi(); // Assuming the method is in a class called KetNoiCSDL.
        String sql = "SELECT * FROM SACH";
        Vector vt;

        try (
            Statement ps = ketNoi.createStatement();
            ResultSet rs = ps.executeQuery(sql)
        ) {
            while (rs.next()) {
                vt = new Vector();
                vt.add(rs.getString("MASACH"));
                vt.add(rs.getString("TUASACH"));
                vt.add(rs.getString("TACGIA"));
                vt.add(rs.getString("NHAXUATBAN"));
                dtm.addRow(vt);
            }
            jTableSach.setModel(dtm);
        } catch (SQLException ex) {
            System.out.println("Loi khi doc du lieu tu bang Sach");
            ex.printStackTrace(); // For debugging.
        } finally {
            try {
                if (ketNoi != null && !ketNoi.isClosed()) {
                    ketNoi.close();
                }
            } catch (SQLException ex) {
                System.out.println("loi doc du lieu");        }
        }
    }
