    private void jButton_SuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_SuaActionPerformed
        // TODO add your handling code here:
        String maSach = jTextField_MaSach.getText().trim();
        String tacGia = jTextField_TacGia.getText().trim();
        String tuaSach = jTextField_TuaSach.getText().trim();
        String nxb = jTextField_NXB.getText().trim();

        // Kiểm tra nếu mã sách trống
        if (maSach.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Vui lòng nhập mã sách để sửa.");
            return; // Thoát khỏi phương thức nếu mã sách trống
        }

        // Kết nối đến cơ sở dữ liệu
        Connection ketNoi = KetNoiCSDL.layKetNoi();
        try {
            // Bước 1: Kiểm tra xem mã sách có tồn tại trong database không
            String checkSql = "SELECT COUNT(*) FROM SACH WHERE MASACH = ?";
            try (PreparedStatement checkStmt = ketNoi.prepareStatement(checkSql)) {
                checkStmt.setString(1, maSach);
                ResultSet rs = checkStmt.executeQuery();
                if (rs.next() && rs.getInt(1) == 0) {
                    javax.swing.JOptionPane.showMessageDialog(this, "Mã sách không tồn tại. Không thể sửa!");
                    return; // Thoát nếu mã sách không tồn tại
                }
            }

            // Bước 2: Xây dựng câu lệnh UPDATE động tùy thuộc vào các trường nhập
            StringBuilder updateSql = new StringBuilder("UPDATE SACH SET ");
            boolean hasUpdate = false; // Biến kiểm tra có cột nào được sửa hay không

            if (!tuaSach.isEmpty()) {
                updateSql.append("TUASACH = ?, ");
                hasUpdate = true;
            }
            if (!tacGia.isEmpty()) {
                updateSql.append("TACGIA = ?, ");
                hasUpdate = true;
            }
            if (!nxb.isEmpty()) {
                updateSql.append("NHAXUATBAN = ?, ");
                hasUpdate = true;
            }

            // Nếu không có gì để sửa, thông báo và thoát
            if (!hasUpdate) {
                javax.swing.JOptionPane.showMessageDialog(this, "Không có thông tin nào để sửa.");
                return;
            }

            // Loại bỏ dấu phẩy thừa cuối câu lệnh
            updateSql.setLength(updateSql.length() - 2);
            updateSql.append(" WHERE MASACH = ?");

            // Bước 3: Thực thi câu lệnh UPDATE
            try (PreparedStatement updateStmt = ketNoi.prepareStatement(updateSql.toString())) {
                int paramIndex = 1;

                // Gắn giá trị tham số vào câu lệnh SQL
                if (!tuaSach.isEmpty()) {
                    updateStmt.setString(paramIndex++, tuaSach);
                }
                if (!tacGia.isEmpty()) {
                    updateStmt.setString(paramIndex++, tacGia);
                }
                if (!nxb.isEmpty()) {
                    updateStmt.setString(paramIndex++, nxb);
                }
                updateStmt.setString(paramIndex, maSach); // Mã sách ở cuối câu lệnh

                int rowsAffected = updateStmt.executeUpdate();
                if (rowsAffected > 0) {
                    javax.swing.JOptionPane.showMessageDialog(this, "Cập nhật sách thành công!");
                    layThongTinDauSach(); // Làm mới bảng dữ liệu
                } else {
                    javax.swing.JOptionPane.showMessageDialog(this, "Cập nhật sách không thành công!");
                }
            }
        } catch (SQLException ex) {
            javax.swing.JOptionPane.showMessageDialog(this, "Lỗi khi sửa sách: " + ex.getMessage());
            ex.printStackTrace();
        } finally {
            try {
                if (ketNoi != null && !ketNoi.isClosed()) {
                    ketNoi.close();
                }
            } catch (SQLException ex) {
                System.out.println("Lỗi đóng kết nối cơ sở dữ liệu");
            }
        }
    }//GEN-LAST:event_jButton_SuaActionPerformed

